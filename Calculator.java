package week1.day2;

public class Calculator {

	public int add(int a, int b, int c)
	{
		int sum = a+b+c;
		return sum;
	}
	public int sub(int a, int b)
	{
		int sub = a - b;
		return sub;

	}
	public double mul(double a, double b)
	{
		double mul = a*b;
		return mul;
	}

	public float div(float a, float b)
	{
		float div = a/b;
		return div;
	}
}
